const uint32_t RCON[] = {
	0x01000000, 0x20000000,
	0x02000000, 0x40000000,
	0x04000000, 0x80000000,
	0x08000000, 0x1b000000,
	0x10000000, 0x36000000
};

union Block {
  uint8_t matrix[4][4];
  uint8_t bytes[16];
  uint32_t rows[4];

  Block(std::string text) {
    assert(text.length() == 16);
    std::memcpy(this, text.data(), 16);
  }

  Block(uint8_t* bytes) {
	for (int c = 0; c < 4; c++) {
		for (int r = 0; r < 4; r++) {
			this->matrix[r][c] = bytes[4*c+r];	
		}
	}
  }

  Block() {}
};

// Print the block for debugging
void dbg(const Block& b, const char* msg = nullptr) {
	if(msg)
		std::cout << "[" << msg << "]\n"; 
	for (int i = 0; i < 4; ++i) {
		for (int j = 0; j < 4; ++j) {
			std::cout << std::hex << std::setw(2) << std::setfill('0')
				<< static_cast<int>(b.matrix[i][j]) << " ";
		}
		std::cout << "\n";
	}
	std::cout << "\n";
}

void subBytes(Block& state) {
	for (int i = 0; i < 16; i++) {
		uint8_t row = state.bytes[i] & 0x0f;
		uint8_t col = (state.bytes[i] & 0xf0) >> 4;
		state.bytes[i] = SBOX[row][col];
	}
};

void shiftRows(Block& state) {
	//TODO: Replace with rotWord()
	state.rows[1] = (state.rows[1] >> 8) | (state.rows[1] << 24);	
	state.rows[2] = (state.rows[2] >> 16) | (state.rows[2] << 16);	
	state.rows[3] = (state.rows[3] >> 24) | (state.rows[3] << 8);	
};

// Muliply byte by 2 in Galois Field (2^8) 
uint8_t mult2(uint8_t x) {
	return (x << 1) ^ ((x & 0x80) ? 0x1b : 0x00);
}

// Muliply byte by 3 in Galois Field (2^8) 
uint8_t mult3(uint8_t x) {
	return mult2(x) ^ x; // 3x == 2x + x (math haxs)
}

void mixColumns(Block& state) {
	uint8_t s0, s1, s2, s3;
	for(int c = 0; c < 4; c++) {
		s0 = state.matrix[0][c];
		s1 = state.matrix[1][c];
		s2= state.matrix[2][c];
		s3 = state.matrix[3][c];

		state.matrix[0][c] = mult2(s0) ^ mult3(s1) ^ s2 ^ s3;
		state.matrix[1][c] = s0 ^ mult2(s1) ^ mult3(s2) ^ s3;
		state.matrix[2][c] = s0 ^ s1 ^ mult2(s2) ^ mult3(s3);
		state.matrix[3][c] = mult3(s0) ^ s1 ^ s2 ^ mult2(s3);
	}
};

void addRoundKey(Block& state, const Block& key) {
	for(int i = 0; i < 16; i++) {
		state.bytes[i] = state.bytes[i] ^ key.bytes[i];
	}
};

uint32_t subWord(uint32_t w) {
	uint32_t sub = 0;
	/* for(int i = 0; i < 4; i++)
		sub = sub | SBOX[(w >> (i*8)) & 0xff] << (i*8); */
	for(int i = 0; i < 4; i++) {
		uint8_t byte = (w >> (i*8)) & 0xff;
		uint8_t row = byte & 0x0f;
		uint8_t col = (byte & 0xf0) >> 4;
		sub = sub | (SBOX[row][col] << (i*8));
	}
	return sub;
};

uint32_t rotWord(uint32_t w) {
	return w = (w >> 8) | (w << 24);	
};

Block* keyExpansion(Block key, int Nr, int Nk) {
	uint32_t* w = new uint32_t[4*(Nr+1)];

	int i = 0;
	while(i < Nk) {
		w[i] = key.rows[i];	
		i++;
	}

	while(i <= 4*Nr+3) {
		uint32_t temp = w[i-1];
		if(i % Nk == 0) {
			temp = subWord(rotWord(temp)) ^ RCON[i / Nk];
		} else if(Nk > 6 && i % Nk == 4) {
			temp = subWord(temp);	
		}

		w[i] = w[i-Nk] ^ temp;
		i++;
	}

	return (Block*)w;
};

Block cipher(Block state, int Nr, Block* w) {

  // Pre-round
  addRoundKey(state, w[0]);

  // Round 0 to Nr-1
  for (int round = 0; round < Nr - 1; round++) {
	std::cout << "---------------------------\n";
	std::string msg = "Round " + std::to_string(round+1) + " start";
	dbg(state, msg.c_str());

    subBytes(state);
	dbg(state, "After Subytes");

    shiftRows(state);
	dbg(state, "After ShiftRows");

    mixColumns(state);
	dbg(state, "After MixColumns");

    addRoundKey(state, w[round]);
	dbg(w[round], "Round Key Value");
  }

  // Last round
  subBytes(state);
  shiftRows(state);
  addRoundKey(state, w[Nr - 1]);
  return state;
};

